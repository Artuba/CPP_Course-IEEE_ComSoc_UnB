#include <iostream> 
using namespace std;

int main(){
	
	// Declaração de variáveis
	double temperatura;
	bool esta_ligado;
	char primeira_letra;
	
	//Múltipla declaração
	int a, b, c;
	double temperatura1, temperatura2, temperatura3;
	
	// Atribuição de variáveis
	temperatura = 32.55;
	esta_ligado = false;
	primeira_letra = 'A';
	
	/*
		Declaração + Atribuição = Inicialização
	*/
	
	// Inicialização de cópia
	char segunda_letra = 'B';
	float pi = 3.1415;
	
	// Inicialização direta
	int alunos(16);
	bool ativo(false);
	
	// Múltipla inicialização
	char c1('a'), c2('b'), c3('c');
	double altura1 = 2.5, altura2 = 3.33, altura3 = 42;
	
	return 0;
}
